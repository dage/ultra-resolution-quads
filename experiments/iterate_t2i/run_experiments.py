#!/usr/bin/env python3
"""
Run one or more named experiments end-to-end:
  1) Generate a dataset config from an experiment template
  2) Render tiles (L0 + L1)
  3) Evaluate seams and write artifacts
  4) Append results to an HTML table report

This is the intended entrypoint for iteration/benchmarking (instead of keeping many
datasets like generative_infinity_zoom_v6, v7, v8, ... around).
"""

from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
import time
from dataclasses import asdict
from pathlib import Path
from typing import Any, Dict, List, Optional

from experiments_catalog import EXPERIMENTS, Experiment


REPO_ROOT = Path(__file__).resolve().parents[2]
THIS_DIR = Path(__file__).resolve().parent


def _run(cmd: List[str]) -> None:
    proc = subprocess.run(cmd, cwd=str(REPO_ROOT), capture_output=True, text=True)
    if proc.returncode != 0:
        raise RuntimeError(f"Command failed ({proc.returncode}): {' '.join(cmd)}\n\nSTDOUT:\n{proc.stdout}\n\nSTDERR:\n{proc.stderr}")


def _write_report(rows: List[Dict[str, Any]], out_path: Path) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)

    def esc(s: Any) -> str:
        text = "" if s is None else str(s)
        return (
            text.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;")
            .replace("'", "&#39;")
        )

    header = [
        "timestamp",
        "experiment",
        "tile_size",
        "denoise",
        "i2i_workflow",
        "fill",
        "inpaint_region",
        "border_ring",
        "mask_blur",
        "seam_mad_avg",
        "vlm_score",
        "artifacts",
    ]

    tr_rows = []
    for r in rows:
        artifacts = r.get("artifacts") or {}
        seam = artifacts.get("seam_detail_rel") or ""
        center = artifacts.get("center_rel") or ""
        links = []
        if center:
            links.append(f'<a href="{esc(center)}">center</a>')
        if seam:
            links.append(f'<a href="{esc(seam)}">seam</a>')
        art_html = " | ".join(links) if links else ""

        tr_rows.append(
            "<tr>"
            + "".join(
                f"<td>{esc(r.get(k))}</td>"
                for k in header[:-1]
            )
            + f"<td>{art_html}</td>"
            + "</tr>"
        )

    html = f"""\
<html>
<head>
  <meta charset="utf-8" />
  <title>iterate_t2i experiment results</title>
  <style>
    body {{ font-family: -apple-system, BlinkMacSystemFont, Segoe UI, Roboto, sans-serif; padding: 20px; }}
    table {{ border-collapse: collapse; width: 100%; }}
    th, td {{ border: 1px solid #ddd; padding: 8px; font-size: 12px; vertical-align: top; }}
    th {{ background: #f6f6f6; position: sticky; top: 0; }}
    tr:nth-child(even) {{ background: #fbfbfb; }}
    code {{ font-family: ui-monospace, SFMono-Regular, Menlo, monospace; }}
  </style>
</head>
<body>
  <h1>iterate_t2i experiment results</h1>
  <p>Generated by <code>experiments/iterate_t2i/run_experiments.py</code>.</p>
  <table>
    <thead>
      <tr>{''.join(f'<th>{h}</th>' for h in header)}</tr>
    </thead>
    <tbody>
      {''.join(tr_rows)}
    </tbody>
  </table>
</body>
</html>
"""
    out_path.write_text(html, encoding="utf-8")


def _evaluate_dataset(dataset_id: str, *, outdir: Path, mock: bool) -> Dict[str, Any]:
    cmd = [
        sys.executable,
        str(THIS_DIR / "evaluate_dataset.py"),
        "--dataset",
        dataset_id,
        "--clean",
        "--outdir",
        str(outdir),
    ]
    if mock:
        cmd.append("--mock")
    _run(cmd)
    return json.loads((outdir / "summary.json").read_text(encoding="utf-8"))


def _generate_dataset(dataset_id: str, exp: Experiment, *, update_index: bool) -> None:
    cmd = [
        sys.executable,
        str(THIS_DIR / "generate_dataset.py"),
        "--id",
        dataset_id,
        "--name",
        exp.name,
        "--description",
        exp.description,
        "--tile-size",
        str(int(exp.tile_size)),
        "--denoise",
        str(float(exp.denoise)),
        "--comfy-server",
        exp.comfy_server,
        "--prompt-t2i",
        exp.prompt_t2i,
        "--prompt-img2img",
        exp.prompt_img2img,
        "--t2i-workflow",
        exp.t2i_workflow,
        "--i2i-workflow",
        exp.i2i_workflow,
        "--mask-blur",
        str(int(exp.mask_blur_px)),
        "--fill",
        exp.fill_strategy,
        "--inpaint-region",
        exp.inpaint_region,
        "--border-ring",
        str(int(exp.border_ring_px)),
        "--inpaint-alpha",
        str(int(exp.inpaint_alpha)),
        "--force",
    ]
    if exp.pad_px is not None:
        cmd.extend(["--pad", str(int(exp.pad_px))])
    if update_index:
        cmd.append("--update-index")
    _run(cmd)


def _cleanup_tiles(dataset_id: str) -> None:
    dataset_dir = REPO_ROOT / "datasets" / dataset_id
    for level in ("0", "1"):
        p = dataset_dir / level
        shutil.rmtree(p, ignore_errors=True)
    shutil.rmtree(dataset_dir / "__pycache__", ignore_errors=True)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--experiment", default=None, help="Experiment key to run (see --list).")
    ap.add_argument("--all", action="store_true", help="Run all experiments in the catalog.")
    ap.add_argument("--list", action="store_true", help="List available experiments.")
    ap.add_argument("--mock", action="store_true", help="Run without a live ComfyUI server.")
    ap.add_argument("--update-index", action="store_true", help="Update datasets/index.json with generated dataset ids.")
    ap.add_argument("--dataset-prefix", default="genzoom_exp_", help="Generated dataset id prefix.")
    ap.add_argument("--keep-tiles", action="store_true", help="Keep generated tiles under datasets/ after evaluation.")
    args = ap.parse_args()

    if args.list:
        for k, e in EXPERIMENTS.items():
            print(f"{k}: {e.name} (tile_size={e.tile_size}, denoise={e.denoise}, i2i={e.i2i_workflow})")
        return 0

    keys: List[str]
    if args.all:
        keys = list(EXPERIMENTS.keys())
    else:
        if not args.experiment:
            raise SystemExit("Provide --experiment <key> or --all (or use --list).")
        if args.experiment not in EXPERIMENTS:
            raise SystemExit(f"Unknown experiment '{args.experiment}'. Use --list.")
        keys = [args.experiment]

    runs_root = REPO_ROOT / "artifacts" / "iterate_t2i"
    report_path = runs_root / "report.html"
    rows_path = runs_root / "rows.json"

    existing: List[Dict[str, Any]] = []
    if rows_path.exists():
        try:
            existing = json.loads(rows_path.read_text(encoding="utf-8"))
        except Exception:
            existing = []

    for key in keys:
        exp = EXPERIMENTS[key]
        dataset_id = f"{args.dataset_prefix}{key}"
        ts = time.strftime("%Y%m%d_%H%M%S")
        outdir = runs_root / "runs" / f"{ts}_{key}"
        outdir.mkdir(parents=True, exist_ok=True)

        _generate_dataset(dataset_id, exp, update_index=bool(args.update_index))
        summary = _evaluate_dataset(dataset_id, outdir=outdir, mock=bool(args.mock))
        if not args.keep_tiles:
            _cleanup_tiles(dataset_id)

        metrics = summary.get("metrics") or {}
        vlm = summary.get("vlm") or {}

        row: Dict[str, Any] = {
            "timestamp": ts,
            "experiment": key,
            "tile_size": exp.tile_size,
            "denoise": exp.denoise,
            "i2i_workflow": exp.i2i_workflow,
            "fill": exp.fill_strategy,
            "inpaint_region": exp.inpaint_region,
            "border_ring": exp.border_ring_px,
            "mask_blur": exp.mask_blur_px,
            "seam_mad_avg": metrics.get("seam_mad_avg"),
            "vlm_score": (vlm.get("score") if isinstance(vlm, dict) else None),
            "artifacts": {
                "center_rel": str(Path("runs") / f"{ts}_{key}" / "level_1_center.png"),
                "seam_detail_rel": str(Path("runs") / f"{ts}_{key}" / "level_1_seam_detail.png"),
                "summary_rel": str(Path("runs") / f"{ts}_{key}" / "summary.json"),
            },
            "summary": summary,
            "experiment_spec": asdict(exp),
        }
        existing.append(row)

    rows_path.write_text(json.dumps(existing, indent=2) + "\n", encoding="utf-8")
    _write_report(existing, report_path)

    print(f"Wrote {rows_path}")
    print(f"Wrote {report_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
